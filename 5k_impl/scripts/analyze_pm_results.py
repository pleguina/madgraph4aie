#!/usr/bin/env python3
"""
analyze_pm_results.py - Extract Program Memory usage from compiled AIE kernels

Parses the Map_Report.csv or work directory to extract PM (Program Memory) usage
for each kernel. Useful for profiling individual function memory footprints.

Usage:
    python3 scripts/analyze_pm_results.py Work_test_pm_profile/
    python3 scripts/analyze_pm_results.py Work_fir_style_16k/
"""

import sys
import os
import csv
from pathlib import Path
from typing import Dict, List, Tuple

def parse_map_report_csv(csv_path: str) -> List[Dict[str, str]]:
    """Parse the Map_Report.csv file generated by AIE compiler."""
    kernels = []
    
    with open(csv_path, 'r') as f:
        reader = csv.DictReader(f)
        for row in reader:
            if row.get('Type') == 'aie':  # Only AIE tiles (not shim/mem tiles)
                kernels.append(row)
    
    return kernels

def extract_pm_from_kernel_data(work_dir: str) -> Dict[str, int]:
    """
    Extract PM usage from kernel data in Work directory.
    
    Returns dict: tile_name -> PM_bytes (total for that tile)
    """
    import re
    pm_data = {}
    work_path = Path(work_dir)
    
    # Pattern to match PM entries: 'function_name' (bytes, PM)
    pm_pattern = re.compile(r"'([^']+)'\s+\((\d+),\s+PM\)")
    
    # Find all AIE core log files (Work/aie/X_Y/X_Y.log)
    aie_dir = work_path / "aie"
    if not aie_dir.exists():
        return pm_data
    
    for log_file in aie_dir.glob("*/*.log"):
        tile_name = log_file.stem  # e.g., "0_0" from "0_0.log"
        tile_pm_total = 0
        
        try:
            with open(log_file, 'r') as f:
                for line in f:
                    match = pm_pattern.search(line)
                    if match:
                        func_name = match.group(1)
                        pm_bytes = int(match.group(2))
                        tile_pm_total += pm_bytes
        except Exception as e:
            print(f"Warning: Could not read {log_file}: {e}")
            continue
        
        if tile_pm_total > 0:
            pm_data[f"tile_{tile_name}"] = tile_pm_total
    
    return pm_data

def parse_memory_string(mem_str: str) -> int:
    """Convert memory string like '8.5 KB' or '1024 bytes' to bytes."""
    mem_str = mem_str.strip().upper()
    
    if 'KB' in mem_str:
        val = float(mem_str.replace('KB', '').strip())
        return int(val * 1024)
    elif 'MB' in mem_str:
        val = float(mem_str.replace('MB', '').strip())
        return int(val * 1024 * 1024)
    elif 'BYTES' in mem_str or 'B' in mem_str:
        val = float(mem_str.replace('BYTES', '').replace('B', '').strip())
        return int(val)
    else:
        # Try direct int parse
        try:
            return int(mem_str)
        except ValueError:
            return 0

def print_pm_report(pm_data: Dict[str, int], title: str = "Program Memory Report"):
    """Pretty-print PM usage report."""
    print("\n" + "=" * 80)
    print(f" {title}")
    print("=" * 80)
    
    if not pm_data:
        print("No PM data found. Make sure to compile with 'make aie' first.")
        return
    
    # Sort by PM usage (descending)
    sorted_tiles = sorted(pm_data.items(), key=lambda x: x[1], reverse=True)
    
    print(f"{'Tile':<40} {'PM Usage':>15} {'% of 16KB':>10}")
    print("-" * 80)
    
    total_pm = 0
    pm_limit = 16 * 1024  # 16 KB per AIE core
    
    for tile_name, pm_bytes in sorted_tiles:
        pm_kb = pm_bytes / 1024.0
        pm_pct = (pm_bytes / pm_limit) * 100
        print(f"{tile_name:<40} {pm_kb:>10.2f} KB {pm_pct:>9.1f}%")
        total_pm += pm_bytes
    
    print("-" * 80)
    num_tiles = len(sorted_tiles)
    avg_pm = total_pm / num_tiles if num_tiles else 0
    print(f"{'TOTAL (' + str(num_tiles) + ' tiles)':<40} {total_pm/1024.0:>10.2f} KB")
    print(f"{'AVERAGE':<40} {avg_pm/1024.0:>10.2f} KB {(avg_pm/pm_limit)*100:>9.1f}%")
    print("=" * 80)
    
    # Find memory hogs (>75% of 16KB)
    hogs = [k for k, pm in sorted_tiles if pm > 12 * 1024]
    if hogs:
        print(f"\n⚠️  WARNING: {len(hogs)} tile(s) exceed 12 KB (75% of 16 KB limit):")
        for tile in hogs[:10]:  # Show top 10
            pm_kb = pm_data[tile] / 1024.0
            print(f"   - {tile}: {pm_kb:.2f} KB")
        if len(hogs) > 10:
            print(f"   ... and {len(hogs)-10} more")
    
    # Find efficient tiles (<50% of 16KB)
    efficient = [k for k, pm in sorted_tiles if pm < 8 * 1024]
    if efficient:
        print(f"\n✓ {len(efficient)} tile(s) are efficient (<8 KB / 50%):")
        if len(efficient) <= 10:
            for tile in efficient:
                pm_kb = pm_data[tile] / 1024.0
                print(f"   - {tile}: {pm_kb:.2f} KB")
        else:
            print(f"   (use --verbose to see all)")
    
    # Statistics
    if num_tiles > 0:
        min_pm = min(pm_data.values())
        max_pm = max(pm_data.values())
        print(f"\n📊 Statistics:")
        print(f"   Min: {min_pm/1024.0:.2f} KB ({(min_pm/pm_limit)*100:.1f}%)")
        print(f"   Max: {max_pm/1024.0:.2f} KB ({(max_pm/pm_limit)*100:.1f}%)")
        print(f"   Avg: {avg_pm/1024.0:.2f} KB ({(avg_pm/pm_limit)*100:.1f}%)")
    
    print()

def main():
    if len(sys.argv) < 2:
        print("Usage: python3 scripts/analyze_pm_results.py <Work_directory>")
        print("\nExamples:")
        print("  python3 scripts/analyze_pm_results.py Work_test_pm_profile/")
        print("  python3 scripts/analyze_pm_results.py Work_fir_style_16k/")
        sys.exit(1)
    
    work_dir = sys.argv[1]
    
    if not os.path.isdir(work_dir):
        print(f"Error: Directory '{work_dir}' not found.")
        print("Make sure to compile with 'make GRAPH_IMPL=<name> aie' first.")
        sys.exit(1)
    
    # Try to extract PM data
    pm_data = extract_pm_from_kernel_data(work_dir)
    
    # Print report
    graph_impl = os.path.basename(work_dir.rstrip('/')).replace('Work_', '').replace('Work', 'hw')
    print_pm_report(pm_data, title=f"Program Memory Report: {graph_impl}")
    
    # If no PM data found, provide guidance
    if not pm_data:
        print("\n⚠️  No PM data found in AIE core logs.")
        print("Try these steps:")
        print(f"  1. Check if AIE logs exist: ls {work_dir}/aie/*/*.log")
        print(f"  2. Verify compilation completed: ls {work_dir}/libadf.a")
        print(f"  3. View detailed report: vitis_analyzer {work_dir}/reports/*.xpe")

if __name__ == '__main__':
    main()
